parser.add_argument("command", nargs="+", help="The command to run")

def pipe(data):
    cmd.cmd(data)

def cmd(cmd):
    """
    Runs a command on the host shell.
    """
    assertTrue(isinstance(cmd, str))
    os.system(cmd)
def main(args):    
    if args:
        args = parser.parse_args(args)
        cmd(" ".join(args.command))
    else:
        parser.print_help()
